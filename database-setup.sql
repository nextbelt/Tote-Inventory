-- Tote Inventory — Database Setup
-- Run this in the Supabase SQL Editor.
--
-- ⚠️  WARNING: This drops and recreates the table — ALL DATA WILL BE LOST.
-- For production changes use versioned migrations instead.

DROP TABLE IF EXISTS totes;

CREATE TABLE totes (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name       TEXT NOT NULL CHECK (char_length(name) <= 100),
  position   TEXT NOT NULL UNIQUE,
  items      JSONB DEFAULT '[]'::jsonb,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Fast lookups by shelf position
CREATE INDEX idx_totes_position ON totes (position);

-- Row Level Security
ALTER TABLE totes ENABLE ROW LEVEL SECURITY;

-- DEVELOPMENT ONLY — allow all operations from any client.
-- Before going to production replace with authenticated policies, e.g.:
--   CREATE POLICY "auth_users" ON totes
--     USING  (auth.uid() IS NOT NULL)
--     WITH CHECK (auth.uid() IS NOT NULL);
CREATE POLICY "Allow all operations on totes" ON totes
  FOR ALL USING (true) WITH CHECK (true);

-- Real-time updates
ALTER PUBLICATION supabase_realtime ADD TABLE totes;
